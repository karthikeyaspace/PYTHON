import os
import shutil

def get_directory_size(path):
    total_size = 0
    for dirpath, dirnames, filenames in os.walk(path):
        for filename in filenames:
            file_path = os.path.join(dirpath, filename)
            total_size += os.path.getsize(file_path)
    return total_size

def delete_node_modules(root_dir):
    total_deleted_size = 0
    total_files = 0
    for dirpath, dirnames, filenames in os.walk(root_dir):
        if 'node_modules' in dirnames:
            node_modules_path = os.path.join(dirpath, 'node_modules')
            try:
                size = get_directory_size(node_modules_path)
                total_deleted_size += size
                shutil.rmtree(node_modules_path)
                print(f"Deleted: {node_modules_path}, Size: {size/(1024*1024)} MB")
                total_files+=1
            except Exception as e:
                print(f"Failed to delete {node_modules_path}: {e}")
    print(f"\nTotal deleted files: {total_files}")
    print(f"Total deleted size: {total_deleted_size/(1024*1024)} MB")
    

if __name__ == "__main__":
    your_root_directory = '/'  
    delete_node_modules(your_root_directory)
